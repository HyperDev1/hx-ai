describe('getCodebaseMapStats', () => {
  test('returns zeros for empty entries', () => {
    const stats = getCodebaseMapStats([]);
    assert.equal(stats.totalFiles, 0);
    assert.equal(stats.totalDirs, 0);
    assert.equal(stats.topLevelEntries, 0);
  });

  test('counts files and dirs correctly', () => {
    const entries = [
      { path: 'src', isDir: true, ext: '', depth: 0 },
      { path: 'src/index.ts', isDir: false, ext: '.ts', depth: 1 },
      { path: 'src/utils.ts', isDir: false, ext: '.ts', depth: 1 },
      { path: 'README.md', isDir: false, ext: '.md', depth: 0 },
    ];
    const stats = getCodebaseMapStats(entries);
    assert.equal(stats.totalFiles, 3);
    assert.equal(stats.totalDirs, 1);
  });

  test('counts files by extension', () => {
    const entries = [
      { path: 'a.ts', isDir: false, ext: '.ts', depth: 0 },
      { path: 'b.ts', isDir: false, ext: '.ts', depth: 0 },
      { path: 'c.md', isDir: false, ext: '.md', depth: 0 },
    ];
    const stats = getCodebaseMapStats(entries);
    assert.equal(stats.filesByExtension['.ts'], 2);
    assert.equal(stats.filesByExtension['.md'], 1);
  });

  test('counts top-level entries', () => {
    const entries = [
      { path: 'src', isDir: true, ext: '', depth: 0 },
      { path: 'README.md', isDir: false, ext: '.md', depth: 0 },
      { path: 'src/index.ts', isDir: false, ext: '.ts', depth: 1 },
    ];
    const stats = getCodebaseMapStats(entries);
    assert.equal(stats.topLevelEntries, 2);
  });
});

// ---------------------------------------------------------------------------
// writeCodebaseMap / readCodebaseMap roundtrip tests
// ---------------------------------------------------------------------------

describe('writeCodebaseMap / readCodebaseMap', () => {
  test('roundtrip write and read preserves file entries', () => {
    const r = makeTmpRepo();
    try {
      const entries = [
        { path: 'src/index.ts', isDir: false, ext: '.ts', depth: 1 },
        { path: 'src/utils.ts', isDir: false, ext: '.ts', depth: 1 },
      ];

      writeCodebaseMap(r.dir, entries);

      assert.ok(existsSync(join(r.hxDir, 'CODEBASE.md')), 'CODEBASE.md should exist');

      const read = readCodebaseMap(r.dir);
      assert.ok(read !== null);
      assert.equal(read.length, 2);
      const paths = read.map((e) => e.path);
      assert.ok(paths.includes('src/index.ts'));
      assert.ok(paths.includes('src/utils.ts'));
    } finally {
      cleanupRepo(r);
    }
  });

  test('readCodebaseMap returns null when CODEBASE.md absent', () => {
    const r = makeTmpRepo();
    try {
      const result = readCodebaseMap(r.dir);
      assert.equal(result, null);
    } finally {
      cleanupRepo(r);
    }
  });

  test('roundtrip preserves directory entries', () => {
    const r = makeTmpRepo();
    try {
      const entries = [
        { path: 'src', isDir: true, ext: '', depth: 0 },
        { path: 'src/index.ts', isDir: false, ext: '.ts', depth: 1 },
      ];

      writeCodebaseMap(r.dir, entries);
      const read = readCodebaseMap(r.dir);
      assert.ok(read !== null);
      const dirEntry = read.find((e) => e.path === 'src');
      assert.ok(dirEntry, 'src directory entry should be present');
      assert.equal(dirEntry.isDir, true);
    } finally {
      cleanupRepo(r);
    }
  });

  test('write overwrites existing CODEBASE.md', () => {
    const r = makeTmpRepo();
    try {
      writeCodebaseMap(r.dir, [{ path: 'old.ts', isDir: false, ext: '.ts', depth: 0 }]);
      writeCodebaseMap(r.dir, [{ path: 'new.ts', isDir: false, ext: '.ts', depth: 0 }]);

      const read = readCodebaseMap(r.dir);
      assert.ok(read !== null);
      assert.equal(read.length, 1);
      assert.equal(read[0].path, 'new.ts');
    } finally {
      cleanupRepo(r);
    }
  });
});
