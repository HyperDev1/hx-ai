describe('updateCodebaseMap', () => {
  test('adds new entries from fresh scan', () => {
    const existing = [
      { path: 'src/index.ts', isDir: false, ext: '.ts', depth: 1 },
    ];
    const fresh = [
      { path: 'src/index.ts', isDir: false, ext: '.ts', depth: 1 },
      { path: 'src/utils.ts', isDir: false, ext: '.ts', depth: 1 },
    ];

    const merged = updateCodebaseMap(existing, fresh);
    assert.equal(merged.length, 2);
    assert.ok(merged.some((e) => e.path === 'src/utils.ts'));
  });

  test('removes stale entries not in fresh scan', () => {
    const existing = [
      { path: 'src/old.ts', isDir: false, ext: '.ts', depth: 1 },
      { path: 'src/index.ts', isDir: false, ext: '.ts', depth: 1 },
    ];
    const fresh = [
      { path: 'src/index.ts', isDir: false, ext: '.ts', depth: 1 },
    ];

    const merged = updateCodebaseMap(existing, fresh);
    assert.equal(merged.length, 1);
    assert.equal(merged[0].path, 'src/index.ts');
    assert.ok(!merged.some((e) => e.path === 'src/old.ts'));
  });

  test('preserves all entries when fresh matches existing', () => {
    const entries = [
      { path: 'a.ts', isDir: false, ext: '.ts', depth: 0 },
      { path: 'b.ts', isDir: false, ext: '.ts', depth: 0 },
    ];
    const merged = updateCodebaseMap(entries, entries);
    assert.equal(merged.length, 2);
  });

  test('returns all fresh entries when existing is empty', () => {
    const fresh = [
      { path: 'src/index.ts', isDir: false, ext: '.ts', depth: 1 },
      { path: 'src/utils.ts', isDir: false, ext: '.ts', depth: 1 },
    ];
    const merged = updateCodebaseMap([], fresh);
    assert.equal(merged.length, 2);
  });
});

