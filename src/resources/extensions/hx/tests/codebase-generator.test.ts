// ---------------------------------------------------------------------------
// Fixture helpers
// ---------------------------------------------------------------------------

interface TmpRepo {
  dir: string;
  hxDir: string;
  write: (relPath: string, content?: string) => void;
  mkdir: (relPath: string) => void;
}

function makeTmpRepo(): TmpRepo {
  const dir = mkdtempSync(join(tmpdir(), 'hx-codebase-test-'));

  // git init so hxRoot() probe works
  spawnSync('git', ['init', '-q'], { cwd: dir });

  const hxDir = join(dir, '.hx');
  mkdirSync(hxDir, { recursive: true });

  return {
    dir,
    hxDir,
    write(relPath: string, content = `// ${relPath}`) {
      const full = join(dir, relPath);
      mkdirSync(join(full, '..'), { recursive: true });
      writeFileSync(full, content, 'utf-8');
    },
    mkdir(relPath: string) {
      mkdirSync(join(dir, relPath), { recursive: true });
    },
  };
}

function cleanupRepo(r: TmpRepo) {
  try { rmSync(r.dir, { recursive: true, force: true }); } catch { /* best-effort */ }
}

