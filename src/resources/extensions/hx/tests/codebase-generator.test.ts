// parseCodebaseMap tests
// ---------------------------------------------------------------------------

describe('parseCodebaseMap', () => {
  test('returns empty array for empty string', () => {
    assert.deepEqual(parseCodebaseMap(''), []);
  });

  test('returns empty array for header-only content', () => {
    const raw = '# Codebase Map\n\nGenerated by HX.\n\n*Generated: 2024-01-01*\n';
    assert.deepEqual(parseCodebaseMap(raw), []);
  });

  test('parses file entries', () => {
    const raw = '# Codebase Map\n\n- `src/index.ts`\n- `src/utils.ts`\n';
    const entries = parseCodebaseMap(raw);
    assert.equal(entries.length, 2);
    assert.equal(entries[0].path, 'src/index.ts');
    assert.equal(entries[0].isDir, false);
    assert.equal(entries[0].ext, '.ts');
    assert.equal(entries[0].depth, 0);
  });

  test('parses directory entries (trailing slash)', () => {
    const raw = '- `src/`\n  - `src/index.ts`\n';
    const entries = parseCodebaseMap(raw);
    assert.equal(entries.length, 2);
    assert.equal(entries[0].path, 'src');
    assert.equal(entries[0].isDir, true);
    assert.equal(entries[1].path, 'src/index.ts');
    assert.equal(entries[1].depth, 1);
  });

  test('parses mixed depths', () => {
    const raw = '- `src/`\n  - `src/lib/`\n    - `src/lib/helpers.ts`\n';
    const entries = parseCodebaseMap(raw);
    assert.equal(entries[0].depth, 0);
    assert.equal(entries[1].depth, 1);
    assert.equal(entries[2].depth, 2);
  });
});

// ---------------------------------------------------------------------------
// generateCodebaseMap tests
// ---------------------------------------------------------------------------

describe('generateCodebaseMap', () => {
  test('generates entries for a simple project', () => {
    const r = makeTmpRepo();
    try {
      r.write('src/index.ts');
      r.write('src/utils.ts');
      r.write('README.md');

      const entries = generateCodebaseMap(r.dir, {});
      const paths = entries.map((e) => e.path);
      assert.ok(paths.includes('README.md'), 'README.md should be in entries');
      assert.ok(paths.some((p) => p.includes('index.ts')), 'index.ts should be in entries');
    } finally {
      cleanupRepo(r);
    }
  });

  test('excludes .hx/ by default', () => {
    const r = makeTmpRepo();
    try {
      r.write('src/index.ts');
      writeFileSync(join(r.hxDir, 'STATE.md'), '# State\n');

      const entries = generateCodebaseMap(r.dir, {});
      const paths = entries.map((e) => e.path);
      assert.ok(!paths.some((p) => p.startsWith('.hx')), '.hx/ should be excluded');
    } finally {
      cleanupRepo(r);
    }
  });

  test('excludes node_modules/ by default', () => {
    const r = makeTmpRepo();
    try {
      r.mkdir('node_modules/some-pkg');
      r.write('node_modules/some-pkg/index.js');
      r.write('src/index.ts');

      const entries = generateCodebaseMap(r.dir, {});
      const paths = entries.map((e) => e.path);
      assert.ok(!paths.some((p) => p.startsWith('node_modules')), 'node_modules/ should be excluded');
    } finally {
      cleanupRepo(r);
    }
  });

  test('respects custom exclude patterns', () => {
    const r = makeTmpRepo();
    try {
      r.write('src/index.ts');
      r.write('generated/output.ts');

      const entries = generateCodebaseMap(r.dir, { excludePatterns: ['generated/'] });
      const paths = entries.map((e) => e.path);
      assert.ok(!paths.some((p) => p.startsWith('generated')), 'generated/ should be excluded');
    } finally {
      cleanupRepo(r);
    }
  });

  test('respects maxFiles limit', () => {
    const r = makeTmpRepo();
    try {
      for (let i = 0; i < 20; i++) r.write(`src/file${i}.ts`);

      const entries = generateCodebaseMap(r.dir, { maxFiles: 5 });
      const fileCount = entries.filter((e) => !e.isDir).length;
      assert.ok(fileCount <= 5, `Expected ≤5 files, got ${fileCount}`);
    } finally {
      cleanupRepo(r);
    }
  });

  test('depth is correct for nested files', () => {
    const r = makeTmpRepo();
    try {
      r.write('a/b/c/deep.ts');

      const entries = generateCodebaseMap(r.dir, { collapseThreshold: 0 });
      const deepEntry = entries.find((e) => e.path.endsWith('deep.ts'));
      assert.ok(deepEntry, 'deep.ts should be present');
      assert.equal(deepEntry.depth, 3);
    } finally {
      cleanupRepo(r);
    }
  });
});

